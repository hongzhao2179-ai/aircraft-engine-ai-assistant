"""
三元组抽取提示词 — 用于指导 LLM 从文档文本中提取 (实体, 关系, 实体) 三元组。
"""

EXTRACTION_SYSTEM_PROMPT = """你是一个知识图谱三元组抽取专家。
你的任务是从给定文本中提取结构化的三元组。

输出格式严格要求为 JSON 数组，每个元素包含:
- subject: 主体实体名称
- predicate: 关系类型
- object: 客体实体名称
- confidence: 置信度 (0.0-1.0)

不要输出除 JSON 数组以外的任何内容。"""


def build_extraction_prompt(text_chunk: str) -> str:
    """构建三元组抽取的用户提示词"""
    return f"""请从以下文本中提取知识三元组(实体-关系-实体):

---文本开始---
{text_chunk}
---文本结束---

注意:
1. 只提取确实存在于文本中的关系，不要臆造
2. 实体名称尽量简洁准确
3. 关系类型用2-4个汉字描述(如"位于"、"属于"、"导致")
4. 如果文本中没有明确的知识关系，返回空数组 []
5. 每个文本片段最多提取 10 个三元组

直接返回 JSON 数组:"""
